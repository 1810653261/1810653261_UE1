public class Aufgabe5_1810653261 {


    public static void main(String[]args) {

        int zahl1=(10);
        int zahl2=(20);

        System.out.println(zahl1++); //bei zahl1++ wird die zahl nach der ausgabe um eins erhöht, bei ++zahl1 wird vor der ausgabe die zahl um eins erhöht.
        System.out.println(zahl2++);

        System.out.println(zahl1);
        System.out.println(zahl2);

        System.out.println(++zahl1);
        System.out.println(++zahl2);

    }

}
